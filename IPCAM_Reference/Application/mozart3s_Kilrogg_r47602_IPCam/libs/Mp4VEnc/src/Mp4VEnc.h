	DWORD dwInHorzOffset;
	DWORD dwInVertOffset;
/* ======================================== */

/* Version 10.0.0.0 modification, 2009.11.25 */
	DWORD dwStreamBusNum;
	DWORD dwFrameBusNum;
/* ======================================== */

	DWORD dwTimeResolution; 	// the VOP time increment resolution
	DWORD dwPacketSize;         // the maximum packet size
	DWORD dwTicksPerTR;
	DWORD dwIntraVOPInterval;
	DWORD dwQuant;
/* Version 8.0.0.0 modification, 2008.01.26 */
/* ======================================== */
	DWORD dwBitRate;
	DWORD dwIntraMBRefreshNum;  // force intra MB number in each inter frame
	DWORD dwMaxPacketCount;
/* Version 7.0.0.0 modification, 2007.12.25 */
/* ======================================== */

/* Version 5.0.0.0 modification, 2006.04.27 */
/* ======================================== */

/* Version 9.0.0.0 modification, 2008.09.18 */
/* ======================================== */

	BOOL bEnableShortHeader;	// the encoding short header mode flag
	BOOL bUseGVOP;
	BOOL bUseCI;
	BOOL bDisableResyncMarker;
	BOOL bEnableDataPartitioned;
	BOOL bEnableRVLC;
/* Version 8.0.0.0 modification, 2008.01.26 */
	BOOL bEnableIntraVOPRefresh;
/* ======================================== */
	BOOL bUseDetailedVPHeader;
	ERateCtrlFlags eRCType;

/* Version 11.0.0.0 modification, 2013.03.28 */
	BYTE *pbyMediaBuff;
	DWORD dwMediaBuffSize;
/* ======================================== */

/* Version 3.0.0.0 modification, 2006.04.20 */
