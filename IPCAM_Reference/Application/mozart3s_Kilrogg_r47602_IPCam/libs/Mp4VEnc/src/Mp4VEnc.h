	EBufTypeFlags eBufTypeFlags;
	FOnSendData	pfnSend;
	FOnEnterCriticalSec pfnEnter;
	FOnLeaveCriticalSec pfnLeave;

	void *pObjectMem;
	BYTE *pbyYFrame; 	        // the pointer to the input Y frame buffer
